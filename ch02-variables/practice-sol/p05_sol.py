"""
실습 5 풀이: 자료형에 따른 연산 차이 확인하기
"""

# [1] + 연산자
print(10 + 20)
print("10" + "20")

# A1: 10 + 20은 숫자 덧셈(30), "10" + "20"은 문자열 이어붙이기("1020")
#     자료형이 다르면 같은 + 연산자라도 동작이 다르다.


# [2] * 연산자
print(3 * 4)
print("하" * 4)

# A2: 3 * 4는 숫자 곱셈(12), "하" * 4는 문자열 반복("하하하하")
#     자료형이 다르면 같은 * 연산자라도 동작이 다르다.


# [3] 자료형이 다르면 오류가 발생할 수 있습니다
# print("점수: " + 100)  # TypeError 발생

# A3: str()로 숫자를 문자열로 변환하면 해결됩니다.
print("점수: " + str(100))


# [4] type()으로 원인 파악하기
a = "100"
b = 100

print(type(a))
print(type(b))

# A4: a는 str, b는 int이므로 서로 다른 자료형끼리 + 연산을 할 수 없어 TypeError가 발생한다.
